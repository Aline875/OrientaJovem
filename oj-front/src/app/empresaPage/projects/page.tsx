"use client";

import { useEffect, useState } from "react";
import Link from "next/link";
import { supabase } from "@/lib/supabase";
import Header from "@/components/Header";
import AppSidebar from "@/components/Sidebar";
import { Button } from "@/components/ui/button";
import {
  Dialog,
  DialogContent,
  DialogDescription,
  DialogHeader,
  DialogTitle,
  DialogTrigger,
} from "@/components/ui/dialog";
import { Input } from "@/components/ui/input";
import { Label } from "@/components/ui/label";
import { Textarea } from "@/components/ui/textarea";
import {
  Select,
  SelectContent,
  SelectItem,
  SelectTrigger,
  SelectValue,
} from "@/components/ui/select";
import { Plus } from "lucide-react";

type Tutor = {
  id_tutor: number;
  nome_tutor: string;
};

type Projeto = {
  id_projeto: number;
  nome_projeto: string;
  tutor: Tutor | null;
};

export default function ListaProjetos() {
  const [projetos, setProjetos] = useState<Projeto[]>([]);
  const [tutores, setTutores] = useState<Tutor[]>([]);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState<string | null>(null);
  const [showForm, setShowForm] = useState(false);
  const [formData, setFormData] = useState({
    nome_projeto: "",
    descricao: "",
    id_tutor: ""
  });
  const [submitting, setSubmitting] = useState(false);

  useEffect(() => {
    fetchProjetos();
    fetchTutores();
  }, []);

  async function fetchProjetos() {
    try {
      setLoading(true);
      const sessao = localStorage.getItem("usuario_sessao");
      if (!sessao) throw new Error("Nenhum usuário logado.");

      const usuario = JSON.parse(sessao);
      if (usuario.tipo !== "empresa") throw new Error("Usuário inválido.");

      const { data, error: supabaseError } = await supabase
        .from("projeto")
        .select(
          `
          id_projeto,
          nome_projeto,
          tutor:id_tutor (
            id_tutor,
            nome_tutor
          )
        `
        )
        .eq("id_empresa", usuario.id);

      if (supabaseError) throw supabaseError;

      const projetosFormatados: Projeto[] = (data ?? []).map(
        (item: {
          id_projeto: number;
          nome_projeto: string;
          tutor: Tutor | Tutor[] | null;
        }) => ({
          id_projeto: item.id_projeto,
          nome_projeto: item.nome_projeto,
          tutor: Array.isArray(item.tutor) ? item.tutor[0] : item.tutor,
        })
      );

      setProjetos(projetosFormatados);
    } catch (err) {
      setError(
        err instanceof Error ? err.message : "Erro ao carregar projetos."
      );
    } finally {
      setLoading(false);
    }
  }

  async function fetchTutores() {
    try {
      const { data, error: supabaseError } = await supabase
        .from("tutor")
        .select("id_tutor, nome_tutor")
        .order("nome_tutor");

      if (supabaseError) throw supabaseError;
      setTutores(data || []);
    } catch (err) {
      console.error("Erro ao carregar tutores:", err);
    }
  }

  async function handleSubmit(e: React.FormEvent) {
    e.preventDefault();
    
    if (!formData.nome_projeto.trim()) {
      alert("Nome do projeto é obrigatório!");
      return;
    }

    try {
      setSubmitting(true);
      const sessao = localStorage.getItem("usuario_sessao");
      if (!sessao) throw new Error("Nenhum usuário logado.");

      const usuario = JSON.parse(sessao);

      const { error: supabaseError } = await supabase
        .from("projeto")
        .insert([
          {
            nome_projeto: formData.nome_projeto.trim(),
            descricao: formData.descricao.trim(),
            id_tutor: formData.id_tutor ? parseInt(formData.id_tutor) : null,
            id_empresa: usuario.id
          }
        ])
        .select();

      if (supabaseError) throw supabaseError;

      // Limpar formulário e fechar modal
      setFormData({ nome_projeto: "", descricao: "", id_tutor: "" });
      setShowForm(false);
      
      // Recarregar lista de projetos
      await fetchProjetos();
      
      alert("Projeto criado com sucesso!");
    } catch (err) {
      console.error("Erro ao criar projeto:", err);
      alert("Erro ao criar projeto. Tente novamente.");
    } finally {
      setSubmitting(false);
    }
  }

  function handleInputChange(e: React.ChangeEvent<HTMLInputElement | HTMLTextAreaElement>) {
    const { name, value } = e.target;
    setFormData(prev => ({
      ...prev,
      [name]: value
    }));
  }

  function handleSelectChange(value: string) {
    setFormData(prev => ({
      ...prev,
      id_tutor: value || ""
    }));
  }

  return (
    <div className="mt-16 min-h-screen bg-gradient-to-t from-[#1A5579] to-[#2A2570] text-white">
      <Header />
      <main className="flex">
        <AppSidebar />
        <section className="flex-1 px-6 py-10">
          <div className="flex justify-between items-center mb-6">
            <h1 className="text-3xl font-bold text-blue-200">Projetos</h1>
            
            <Dialog open={showForm} onOpenChange={setShowForm}>
              <DialogTrigger asChild>
                <Button className="bg-white/10 backdrop-blur-sm text-blue-200 border border-white/20 hover:bg-white/20 transition-all duration-300">
                  <Plus className="w-4 h-4 mr-2" />
                  Novo Projeto
                </Button>
              </DialogTrigger>
              <DialogContent className="sm:max-w-[425px] bg-white/10 backdrop-blur-sm border border-white/20 text-blue-200">
                <DialogHeader>
                  <DialogTitle className="text-blue-200">Criar Novo Projeto</DialogTitle>
                  <DialogDescription className="text-blue-200/80">
                    Preencha as informações do projeto. O nome é obrigatório.
                  </DialogDescription>
                </DialogHeader>
                
                <form onSubmit={handleSubmit} className="space-y-4">
                  <div className="space-y-2">
                    <Label htmlFor="nome_projeto" className="text-blue-200">Nome do Projeto *</Label>
                    <Input
                      id="nome_projeto"
                      name="nome_projeto"
                      value={formData.nome_projeto}
                      onChange={handleInputChange}
                      placeholder="Digite o nome do projeto"
                      className="bg-white/10 backdrop-blur-sm border-white/20 text-blue-200 placeholder:text-blue-200/60"
                      required
                    />
                  </div>

                  <div className="space-y-2">
                    <Label htmlFor="descricao" className="text-blue-200">Descrição</Label>
                    <Textarea
                      id="descricao"
                      name="descricao"
                      value={formData.descricao}
                      onChange={handleInputChange}
                      placeholder="Descreva o projeto (opcional)"
                      className="bg-white/10 backdrop-blur-sm border-white/20 text-blue-200 placeholder:text-blue-200/60"
                      rows={3}
                    />
                  </div>

                  <div className="space-y-2">
                    <Label htmlFor="tutor" className="text-blue-200">Tutor</Label>
                    <div className="flex gap-2">
                      <Select 
                        value={formData.id_tutor || undefined} 
                        onValueChange={handleSelectChange}
                      >
                        <SelectTrigger className="flex-1 bg-white/10 backdrop-blur-sm border-white/20 text-blue-200">
                          <SelectValue placeholder="Selecione um tutor (opcional)" />
                        </SelectTrigger>
                        <SelectContent className="bg-white/10 backdrop-blur-sm border-white/20">
                          {tutores.map((tutor) => (
                            <SelectItem 
                              key={tutor.id_tutor} 
                              value={tutor.id_tutor.toString()}
                              className="text-blue-200 hover:bg-white/20"
                            >
                              {tutor.nome_tutor}
                            </SelectItem>
                          ))}
                        </SelectContent>
                      </Select>
                      {formData.id_tutor && (
                        <Button
                          type="button"
                          variant="outline"
                          size="sm"
                          onClick={() => setFormData(prev => ({ ...prev, id_tutor: "" }))}
                          className="bg-white/10 backdrop-blur-sm border-white/20 text-blue-200 hover:bg-white/20"
                        >
                          Limpar
                        </Button>
                      )}
                    </div>
                  </div>

                  <div className="flex gap-2 pt-4">
                    <Button
                      type="submit"
                      disabled={submitting}
                      className="flex-1 bg-white/10 backdrop-blur-sm border border-white/20 text-blue-200 hover:bg-white/20 transition-all duration-300"
                    >
                      {submitting ? "Criando..." : "Criar Projeto"}
                    </Button>
                    <Button
                      type="button"
                      variant="outline"
                      onClick={() => setShowForm(false)}
                      className="flex-1 bg-white/10 backdrop-blur-sm border-white/20 text-blue-200 hover:bg-white/20"
                    >
                      Cancelar
                    </Button>
                  </div>
                </form>
              </DialogContent>
            </Dialog>
          </div>

          {loading ? (
            <p className="text-blue-200">Carregando...</p>
          ) : error ? (
            <p className="text-red-400">{error}</p>
          ) : projetos.length === 0 ? (
            <p className="text-blue-200">Nenhum projeto encontrado.</p>
          ) : (
            <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-3 gap-6">
              {projetos.map((projeto) => (
                <Link
                  key={projeto.id_projeto}
                  href={`/empresaPage/projects/${projeto.id_projeto}`}
                  className="bg-white/10 backdrop-blur-sm text-blue-200 p-6 rounded-lg border border-white/20 hover:bg-white/20 transition-all duration-300 block"
                >
                  <h2 className="font-semibold text-xl mb-1 text-blue-200">
                    {projeto.nome_projeto}
                  </h2>
                  <p className="text-sm text-blue-200/80">
                    Tutor: {projeto.tutor?.nome_tutor || "Não definido"}
                  </p>
                </Link>
              ))}
            </div>
          )}
        </section>
      </main>
    </div>
  );
}